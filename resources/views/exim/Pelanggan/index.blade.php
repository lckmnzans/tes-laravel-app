                                location.reload(); // Refresh halaman untuk menampilkan data terbaru
                            } else {
                                this.showMessage('Gagal menghapus pelanggan.', 'error');
                            }
                        } catch (error) {
                            this.showMessage('Terjadi kesalahan. Coba lagi.', 'error');
                        }
                    },

